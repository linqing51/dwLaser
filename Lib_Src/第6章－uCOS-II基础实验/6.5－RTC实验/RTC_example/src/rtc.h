struct  date    {
    uint16      da_year;        /* 公元年    */
    uint8       da_mon;         /* 月        */
    uint8       da_day;         /* 月中日期   */
    uint8       da_dow;         /* 星期中日期 */
};

struct  time    {
    uint8       ti_hour;        /* 时   */
    uint8       ti_min;         /* 分   */
    uint8       ti_sec;         /* 秒   */
    uint8       ti_hund;        /* 毫秒 */
};

