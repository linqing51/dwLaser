		RES(R_STANDBY_KEY_POWER_CH0_DEC_DOWN);
		RES(R_STANDBY_KEY_POWER_CH0_DEC_UP);
		RES(R_STANDBY_KEY_POWER_CH1_ADD_DOWN);		
		RES(R_STANDBY_KEY_POWER_CH1_ADD_UP);
