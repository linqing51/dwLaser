		epromWriteOneByte((CONFIG_EPROM_SIZE - 3), NVRAM0[TM_START + 2]);//写EPROM
		epromWriteOneByte((CONFIG_EPROM_SIZE - 4), NVRAM0[TM_START + 3]);//写EPROM
