			SET(R_DCHMI_RESTORE_DONE);
			//设置HMI页面
			NVRAM0[EM_HMI_OPERA_STEP] = FSMSTEP_CHECK_PROCESSOR;
			NVRAM0[EM_DC_PAGE] = GDDC_PAGE_POWERUP;//HMI页面
			SetProgressValue(GDDC_PAGE_POWERUP, GDDC_PAGE_POWERUP_PROGRESS_CHECK_INFO, 0);
